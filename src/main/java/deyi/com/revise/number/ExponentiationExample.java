package deyi.com.revise.number;

public class ExponentiationExample {
    public static void main(String[] args) {
        // 计算2的3次方，即2的指数幂
        double base = 4;
        double exponent = 0.5;
        double result = 0.4 * Math.pow(base, exponent);

        System.out.println(base + "的" + exponent + "次方是：" + result);
    }
}
