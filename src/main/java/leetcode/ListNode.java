/**
 * @Author liudy23
 * @Create 2022/2/9 8:46
 * Definition for singly-linked list
 */
public class ListNode {
    int value;
    ListNode next;
    ListNode(){}
    ListNode(int value){
        this.value = value;
    }
    ListNode(int value, ListNode next){
        this.value = value;
        this.next = next;
    }
}
